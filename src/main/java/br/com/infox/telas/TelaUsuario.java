    // MÉTODO CONSULTAR USUÁRIOS (SELECT) -  
    private void consultar () {
        String sql = "select * from tbusuarios where iduser=?";
        try {
            pst=conexao.prepareStatement (sql);
            pst.setString(1,txtUsuId.getText());
            rs=pst.executeQuery();
            if (rs.next()) {
                txtUsuNome.setText(rs.getString(2));
                txtUsuFone.setText (rs.getString(3));
                txtUsuLogin.setText (rs.getString(4));
                txtUsuSenha.setText (rs.getString(5));
                
                // a linnha abaixo refere ao combobox
                cboUsuPerfil.setSelectedItem(rs.getString(6));
                
    //código abaixo caso não encontre nenhum usuário com o nome cadastrado
            } else {
                JOptionPane.showMessageDialog(null,"Usuário não cadastrado");
                // as linhas abaixo limpam os campos 
                txtUsuNome.setText(null);
                txtUsuFone.setText (null);
                txtUsuLogin.setText (null);
                txtUsuSenha.setText (null);
                cboUsuPerfil.setSelectedItem(null);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
        
    }
    
// MÉTODO ADICIONAR USUÁRIOS (INSERT)
private void adicionar() {
    String sql = "INSERT INTO tbusuarios (iduser, usuario, fone, login, senha, perfil) VALUES (?, ?, ?, ?, ?, ?)";
    
    try {
        pst = conexao.prepareStatement(sql);
        pst.setString(1, txtUsuId.getText());
        pst.setString(2, txtUsuNome.getText());
        pst.setString(3, txtUsuFone.getText());
        pst.setString(4, txtUsuLogin.getText());
        pst.setString(5, txtUsuSenha.getText());
        pst.setString(6, cboUsuPerfil.getSelectedItem().toString());
        // Executa a inserção e retorna o número de linhas afetadas
        int adicionado = pst.executeUpdate();
        // Se pelo menos uma linha foi inserida, exibe a mensagem de sucesso
        if (adicionado > 0) {
            JOptionPane.showMessageDialog(null, "Usuário cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            // Limpa os campos do formulário após a inserção
            limparCampos();
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao adicionar usuário: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
}

// criando o método para alterar dados 
   private void alterar() {
        String sql = "UPDATE tbusuarios SET usuario=?, fone=?, login=?, senha=?, perfil=? WHERE iduser=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuNome.getText());
            pst.setString(2, txtUsuFone.getText());
            pst.setString(3, txtUsuLogin.getText());
            pst.setString(4, txtUsuSenha.getText());
            pst.setString(5, cboUsuPerfil.getSelectedItem().toString());
            pst.setString(6, txtUsuId.getText());
            
            int alterado = pst.executeUpdate();
            if (alterado > 0) {
                JOptionPane.showMessageDialog(null, "Usuário atualizado com sucesso!");
                limparCampos();
            } else {
                JOptionPane.showMessageDialog(null, "Nenhum usuário encontrado para atualização.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar usuário: " + e.getMessage());
        } finally {
            fecharConexao();
        }
    }
   

   
     private void deletar() {
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja remover este usuário?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM tbusuarios WHERE iduser=?";
            try {
                pst = conexao.prepareStatement(sql);
                pst.setString(1, txtUsuId.getText());
                
                int apagado = pst.executeUpdate();
                if (apagado > 0) {
                    JOptionPane.showMessageDialog(null, "Usuário removido com sucesso!");
                    limparCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "Usuário não encontrado!");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro ao excluir usuário: " + e.getMessage());
            } finally {
                fecharConexao();
            }
        }
    }



